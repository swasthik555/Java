package wrapperClasses;

public class Wrapper_Classes {

	@SuppressWarnings("removal")
	public static void main(String[] args) {

		Integer a = new Integer(45);
		System.out.println(a);

		Double b = new Double(3.68);
		System.out.println(b);

		Character c = new Character('s');
		System.out.println(c);

		Boolean d = new Boolean(false);
		System.out.println(d);

		Float e = new Float(4.56F);
		System.out.println(e);

		Long f = new Long(4967L);
		System.out.println(f);

		Byte g = new Byte((byte) 2); // convert to byte data type
		System.out.println(g);

		Short h = new Short((short) 23); // convert to short data type
		System.out.println(h);

	}

}
