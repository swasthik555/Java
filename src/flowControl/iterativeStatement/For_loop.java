package flowControl.iterativeStatement;

public class For_loop {

	public static void main(String[] args) {

		for(int i=0;i<5;i++) { //Declaration and Initialization
			System.out.println("Sanjay : "+i);
			
		}
		System.out.println("_____________________________");
		//-------------------------------------
		
		int a =5, b=6; //Declaration
		for(;a<b;a++) { //Initialization
			System.out.println("Munavara");
		}
		System.out.println("_____________________________");
		//----------------------------------------
		// a = 5, b = 6;
		for(;a>b;) {
			System.out.println("Mub");
		}
		System.out.println("Out of for loop");
		System.out.println("______________________________");
		//----------------------------------------
		
		for(int j=5;j>0;j--) {
			System.out.println("Udupi "+j);
		}
		System.out.println("_______________________________");
		//-----------------------------------------
		
		
	}}