package arrays;

public class arrays_and_dataTypes {

	public static void main(String[] args) {
		int [] a = {1,2,3};
		
		long[] b = {9L,123L,56L};
		
		char[] c = {'s','w','u','i'};
		
		String[] d = {"Sanjay","Sheera","Mubasheera","Munavara"};
		
		boolean[] e = {true, false, false, true};
		
		float[] f = {1.23F, 4.67F, 3.69F};
		
		byte[] g = {1,5,2,7};
		
		short[] h = {6,3,2,8};
		
		System.out.println(a[1]+" "+b[1]+" "+c[1]+" "+d[1]+" "+e[1]+" "+f[1]+" "+g[1]+" "+h[1]);

	}

}
