package operators.AssignmentOperators;

public class SimpleAssignmentOperator {

	public static void main(String[] args) {
		int a; //Declaration statement
		a = 3; //initialization statement
		System.out.println(a);
		
		System.out.println("----------------------------");
		
		int j =4; //Simple
		int k = 7; //Simple
		
		int l = j+k; //Dynamic initialization
		System.out.println(l);
		
		System.out.println("----------------------------");
		
		int z = 5;
		int x = z; //different way of initialization
		System.out.println(z);
		System.out.println(x);
		
		System.out.println("--------------------------");
		
		int q=3,w=5,e=1,r=8;
		
		System.out.println(q);
		System.out.println(w);
		System.out.println(e);
		System.out.println(r);
		
		System.out.println("--------------------------");
		
		int p,o,i,u;
		p = o = i = u =7;
		
		System.out.println(p);
		System.out.println(o);
		System.out.println(i);
		System.out.println(u);
		
		System.out.println("----------------------------");
		System.out.println("Chain assignment Operator");
		int s,t,y=5;
		s = t = y; //Chain assignment Operator
		
		System.out.println(s);
		System.out.println(t);
		System.out.println(y);
		
		System.out.println("-------------------------");
		
		
	

	}

}
