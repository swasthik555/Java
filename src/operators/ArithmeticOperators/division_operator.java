package operators.ArithmeticOperators;

public class division_operator {

	public static void main(String[] args) {
		System.out.println(20 / 5); //4
		
		//********************
		
		System.out.println(3.5F / 1.5F); //result updated to floating literals

		//********************
		
		System.out.println(20.25 / 5.25); 
		
		//*****************************88
		
		System.out.println('x' / '2'); // 120/50 = 2
		System.out.println('x' % '2'); //120%50 = 20
		
		//*********************
		
		int a = 20;
		int b = 5;
		System.out.println(a/b);
		
		//****************
		
		int c = 20;
		int d = 4;
		int div = c/d;
		System.out.println(div);
		
		//***************************
		
		System.out.println(3.69 / 3);
		
		//***************************
		
		System.out.println(3.69 / 1.23F); //double
		
		//******************************
		
		System.out.println('d' / 5); //100/5 = 20
		
		
	}

}
